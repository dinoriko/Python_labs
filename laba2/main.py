import math

def calculate_expression(a, b):

    # Використовуємо функції sin та додавання/віднімання чисел
    z = math.sin(a + b) * math.sin(a - b)
    return z

def running_days(m, k):

    days = 0
    while m <= 50:
        # Щодня збільшуємо пробіг на заданий відсоток
        m += m * (k / 100)
        days += 1
    return days

# Перше завдання: обчислення виразу
print("\n=== Завдання 1: Обчислення виразу z ===")
a = float(input("Введіть значення a: "))
b = float(input("Введіть значення b: "))
z = calculate_expression(a, b)  # Викликаємо функцію для обчислення z
print(f"Значення виразу z = sin(a + b) * sin(a - b): {z:.4f}")

# Друге завдання: норма пробігу
print("\n=== Завдання 2: Норма пробігу ===")
m = float(input("Введіть початкову норму пробігу (М км): "))
k = float(input("Введіть відсоток збільшення пробігу (К%): "))
# Викликаємо функцію для визначення кількості днів
days = running_days(m, k)
print(f"Кількість днів, через які норма пробігу перевищить 50 км: {days}")

