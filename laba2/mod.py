def calculate_running_days():
    print("\nОбчислення кількості днів для досягнення норми пробігу > 50 км")
    try:
        M = float(input("Введіть норму пробігу за 1-й день (км): "))
        K = float(input("Введіть щоденне збільшення норми пробігу (%): "))

        if M <= 0 or K <= 0:
            print("Помилка: значення M і K мають бути більше 0.")
            return

        days = 1
        while M <= 50:
            M += M * (K / 100)
            days += 1

        print(f"Кількість днів для досягнення пробігу більше 50 км: {days}")
    except ValueError:
        print("Помилка: введіть числові значення M та K.")
